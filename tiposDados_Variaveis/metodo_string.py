a = "Hello world"

print(a.islower()) #islower,é uma forma de perguntar a funcão se o resultado dela é minusculo ou não
print(a.lower()) #lower ja é a ação direta de transformar todas as letras em minusculo
print(a.upper()) # upper é feito para converter os valores para maiusculo
print(a.swapcase())# swapcase faz uma troca de caracteres transformando todos as letras minusculas em maiusculas e vice-versa