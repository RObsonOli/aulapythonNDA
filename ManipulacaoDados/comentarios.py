# Este programa calcula a soma de dois numeros

def soma(a, b):
    """
    Esta função retorna a soma de dois números
    
    Parâmetros:
    a (int, float): O primeiro numero
    b (int, float): O segundo numero
    
    Retorna:
    int, float: A soma de a e b 
    """
    return a + b

# Chamando a função soma e exibindo o resultado
resultado = soma(5, 3)
print("A soma é:", resultado) # Exibe: A soma é: 8